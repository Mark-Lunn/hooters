                <p>It is seen by the brand, a sister company to KFC, as a model that could roll out across the rest of the country.</p>
                <p> Nick Dawson, general manager, Taco Bell UK &amp; Europe, said: “Sheffield is a great place for us to start. It is the UK’s fifth largest city with a thriving student population and Ecclesall Road just felt right. It’s an eating and drinking destination, it’s residential and a nice place. Our franchise partner also has a great local track record.” </p>
                <p>Taco Bell opens at 10.30am tomorrow and  is targeting the ‘Millennials’, the young and young-at-heart looking for something new and exciting at an affordable price. Tacos, burritos and quesadillas are made to order and cost under £5.</p>
