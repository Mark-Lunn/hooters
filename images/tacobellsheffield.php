                <p><strong>UK’S FIRST HIGH STREET TACO BELL ARRIVES ON ‘ECCY’ ROAD</strong><br />
                  Bringing the Mexican flavour to Sheffield, Taco Bell is here! </p>
                <p>Taco Bell – the world’s largest Mexican-inspired restaurant chain – will open its doors to the people of South Yorkshire with the launch of its first UK High Street restaurant on the buzzing Ecclesall Road, Sheffield. Bringing with them their famous tacos, burritos and quesadillas, Taco Bell have pulled out all the stops with this state of the art, 80 seat, dual floor restaurant with free wifi and even charging points for customers’ phones. <br />
                </p>
                <p>The restaurant design is the first of its kind and marries a youthful, urban look with influences from Mexico and California bringing a spot of sunshine to the streets of South Yorkshire, encouraging locals to ‘Live Más’ or ‘get more out of life’. Customers can look forward to trying something new, with delicious, made-to-order classics like Crunchy Tacos, Crunchwrap Supremes, Grilled Stuft Burritos and Chicken Quesadillas, along with an exclusively adapted menu just for the UK including delicious seasoned Mexican fries and Churros with chocolate sauce.</p>
                <p>Taco Bell brings with it over fifty years of experience as the market leader in Mexican-style food, and a reputation for quality ingredients, competitive prices, and first rate customer service - which in this restaurant will be provided by 27 eager new employees recruited from the local area. Customers want choice, and Taco Bell brings something different to the High Street, as well as customers’ taste buds, creating an experience and relationship that goes beyond food.</p>
                <p>Nick Dawson, General Manager, Taco Bell UK &amp; Europe, said:</p>
                <p>“For years we’ve had fans from cities in the UK requesting a Taco Bell on their High Street, and Sheffield was high up on the hit list of demand.  As the UK’s fifth largest city, with a huge student population, Sheffield is a great location for our first High Street Taco Bell restaurant and we are proud to be partnering with our franchisee Northgate Fast Foods to add a new flavour to the restaurant options in the city.”</p>
                <p>Part of Yum! Brands, the world’s largest restaurant company, Taco Bell is one of the biggest quick service restaurant brands in the United States serving more than 2 billion customers each year in the US, selling over 2 billion tacos and 1.5 billion burritos in the process. This latest move onto the UK high Street kicks off the start of a wider expansion plan in the UK over the coming years.</p>
                <p>Currently, there are more than 250 Taco Bell restaurants outside of the United States and approximately 6,500 restaurants in the United States, and Yum! is optimistic about the long–term potential of growing Taco Bell internationally and in the UK as part of that.  To learn more about Taco Bell UK or the Taco Bell menu please go to www.tacobelluk.co.uk or connect with them at Facebook.com/TacoBellUK and @TacoBellUK on Twitter.<br />
                </p>
                <p><img src="images/taco-bell-sheffield.jpg" alt="Taco Bell Sheffield" width="595" height="503" /></p>
